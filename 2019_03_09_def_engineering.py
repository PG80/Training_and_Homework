# calculation of square of rectanglar
def square_of_smth(buba1, buba2):
    square = buba1 * buba2
    return square

square1 = square_of_smth(20, 20)
square2 = square_of_smth(70, 80)

print('The_square_of_first_figure ', square1)
print('The_square_of_second_figure ', square2)


# calculation of perimeter of rectanglar
def perimeter_of_smth(buba1, buba2):
    perimeter = (buba1 + buba2) * 2
    return perimeter

perimeter1 = perimeter_of_smth(20, 20)
perimeter2 = perimeter_of_smth(70, 80)

print('The_perimeter_of_first_figure ', perimeter1)
print('The_perimeter_of_second_figure ', perimeter2)


