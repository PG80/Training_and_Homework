def statistic(data1, data2, data3, data4):
    statistic_everage = (data1 + data2 + data3 + data4) / 4
    return statistic_everage

first_everage_statistic = statistic(12, 15, 11, 13)
second_everage_statistic = statistic(10, 11, 11, 12)
third_everage_statistic = statistic(121, 159, 115, 130)
print(first_everage_statistic)
print(second_everage_statistic)
print(third_everage_statistic)
